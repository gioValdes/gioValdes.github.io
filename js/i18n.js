// Language state
let currentLang = 'en';

// Initialize language on load
document.addEventListener('DOMContentLoaded', function () {
    // Check for saved language preference
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    setLanguage(savedLang);

    // Create language switcher if not exists
    createLanguageSwitcher();
});

function createLanguageSwitcher() {
    // Check if nav-integrated switcher already exists
    const navSwitcher = document.querySelector('.nav-lang-switcher');
    if (navSwitcher) {
        // Use existing nav-integrated switcher
        setupLanguageButtons(navSwitcher);
        return;
    }

    // Fallback: create floating switcher if nav doesn't exist
    const langSwitcher = document.createElement('div');
    langSwitcher.className = 'language-switcher';
    langSwitcher.innerHTML = `
        <button class="lang-btn ${currentLang === 'en' ? 'active' : ''}" data-lang="en">EN</button>
        <button class="lang-btn ${currentLang === 'es' ? 'active' : ''}" data-lang="es">ES</button>
        <button class="lang-btn ${currentLang === 'pt' ? 'active' : ''}" data-lang="pt">PT</button>
    `;

    document.body.appendChild(langSwitcher);
    setupLanguageButtons(langSwitcher);
}

function setupLanguageButtons(container) {
    container.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function () {
            const lang = this.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('preferredLang', lang);

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    const t = translations[lang];

    // Hero
    updateIfExists('.hero-title', t.heroTitle);
    updateIfExists('.hero-subtitle', t.heroSubtitle);
    updateIfExists('.hero-description', t.heroDescription);
    updateIfExists('.scroll-indicator span', t.scrollText);

    // About
    updateIfExists('.about-title', t.aboutTitle);
    updateIfExists('.about-text:nth-of-type(1)', t.aboutText1, true);
    updateIfExists('.about-text:nth-of-type(2)', t.aboutText2, true);
    updateIfExists('.about-text:nth-of-type(3)', t.aboutText3, true);
    updateIfExists('.stat-item:nth-child(1) .stat-label', t.yearsIOS);
    updateIfExists('.stat-item:nth-child(2) .stat-label', t.yearsAndroid);
    updateIfExists('.stat-item:nth-child(3) .stat-label', t.yearsSoftwareDev);

    // Awards
    const sectionTitles = document.querySelectorAll('.awards-section .section-title');
    if (sectionTitles[0]) sectionTitles[0].textContent = t.awardsTitle;
    if (sectionTitles[1]) sectionTitles[1].textContent = t.certificationsTitle;

    // Footer
    updateIfExists('.footer-text', t.footerText);

    // Translate all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.hasAttribute('data-i18n-html')) {
                el.innerHTML = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });
}

function updateIfExists(selector, text, isHTML = false) {
    const element = document.querySelector(selector);
    if (element) {
        if (isHTML) {
            element.innerHTML = text;
        } else {
            element.textContent = text;
        }
    }
}
